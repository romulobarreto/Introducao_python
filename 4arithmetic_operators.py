# + (adição) -> Soma dois valores.
resultado0 = 10 + 5  # 15
print(resultado0)

# - (Subtração) -> Subtrai um valor do outro.
resultado1 = 10 - 5  # 5
print(resultado1)

# * (Multiplicação) -> Multiplica dois valores.
resultado2 = 10 * 5  # 50
print(resultado2)

# / (Divisão) -> Sempre retorna um número decimal (float).
resultado3 = 10 / 3  # 3.3333333333333335
print(resultado3)

# ** (Exponenciação) -> Eleva um número à potência de outro.
resultado4 = 2 ** 3  # 2³ = 8
print(resultado4)

# % (Módulo) -> Retorna o resto da divisão.
resultado5 = 10 % 3  # Resto de 10 ÷ 3 é 1
print(resultado5)

# // (Divisão Inteira) -> Divide e retorna apenas a parte inteira (descarta os decimais).
resultado6 = 10 // 3  # 3 (descarta o .333...)
print(resultado6)

# math.sqrt() (Raiz Quadrada) → Calcula a raiz quadrada de um número. Para usar, é preciso importar o módulo math:
import math
resultado7 = math.sqrt(25)  # 5.0
print(resultado7)
